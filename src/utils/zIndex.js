// 全站共用的 z-index 分層表。
//
// 背景：稽核發現整個 app 有 13 種不同的 zIndex 數字硬編在各處（0, 1, 2, 100, 200, 201,
// 300, 500, 600, 998, 999, 9999, 10000），彼此沒有共同的量級規劃——同樣是「modal 疊在
// overlay 上面」這件事，不同檔案各自選了完全不相關的數字（100/200/500/998 都用過），純屬
// 巧合才沒疊錯層。這裡把「這個數字代表哪一種視覺層級」講清楚，之後新增 UI 只要照層級挑值，
// 不必再憑感覺猜一個「看起來夠大」的數字。
//
// 各層級對應到目前程式碼裡實際存在的用法（遷移前的原始數值供對照）：
//   DROPDOWN        — 頁內下拉選單／自動完成建議清單（目前 14 個檔案內尚無硬編用例，先保留
//                      這個量級給未來用，避免屆時又隨便挑一個數字）
//   FLOATING        — 沒有背景遮罩的浮動按鈕／浮動工具列（原本都用 100：POSPage 的購物車
//                      浮動鈕、InventoryPage 的批次編輯浮動工具列）
//   DRAWER_OVERLAY  — 由畫面邊緣滑入的抽屜／底部彈出面板「背後的遮罩」（原本 App.jsx 手機版
//                      側邊選單用 998、POSPage 手機版購物車底部面板用 200，兩處數字不同但其實
//                      是同一種 UI 模式）
//   DRAWER          — 抽屜／底部面板本體（原本 999 或 201）
//   MODAL_OVERLAY   — 置中 modal 對話框「背後的遮罩」——共用 <Modal> 元件預設值（原本 100、
//                      200、500、998 都出現過，其實都是同一種「置中卡片 + 遮罩」樣式）
//   MODAL           — 置中 modal 對話框本體——共用 <Modal> 元件預設值（原本 999 或無明確值，
//                      靠 DOM 巢狀順序疊在遮罩上面）
//   TOAST           — 畫面下方浮出的短暫提示訊息／snackbar（原本 300 或 600，兩處也是同一種
//                      UI 模式卻用了不同數字）
//   SYSTEM_OVERLAY  — 全站等級、會擋住整個畫面互動的系統提示（例如 App.jsx 登入後雲端同步中
//                      的全螢幕遮罩，原本 9999）
//   SYSTEM_BANNER   — 全站等級、必須蓋過所有其他內容的常駐警示列（例如 App.jsx 的
//                      localStorage 寫入失敗警示列，原本 10000）——整份表裡最高的一層
//
// 刻意不管的例外：0 / 1 / 2（LoginScreen.jsx、DashboardPage.jsx）——這些是單一元件內部、
// 相對於自己兄弟節點的區域性堆疊（例如卡片內裝飾背景蓋在文字下面），不會跟其他元件的浮動層
// 互相競爭，所以不屬於這張「全站共用」表的範圍，也不在本次重構涉及的 14 個檔案內。
export const Z = {
  DROPDOWN: 50,
  FLOATING: 100,
  DRAWER_OVERLAY: 400,
  DRAWER: 401,
  MODAL_OVERLAY: 500,
  MODAL: 501,
  TOAST: 700,
  SYSTEM_OVERLAY: 900,
  SYSTEM_BANNER: 950,
}
